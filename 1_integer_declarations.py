"""
Integer Declarations.
"""

# Declare an integer variable
integer_variable = 10

# Integers, just like strings, are immutable.
# This does NOT mean that the value of the integer cannot be changed.

integer_variable = 20

# Integers support all the math operations that you would expect.

# Addition
integer_variable = 10 + 20
print(integer_variable)

# Subtraction
integer_variable = 20 - 10
print(integer_variable)

# Multiplication
integer_variable = 10 * 20
print(integer_variable)

# Division
integer_variable = 20 / 10
print(integer_variable)

# Modulus
integer_variable = 20 % 10
print(integer_variable)

# Exponentiation
integer_variable = 10 ** 2
print(integer_variable)

# Floor division. This means rounded down to the nearest integer.
integer_variable = 20 // 10
print(integer_variable)

